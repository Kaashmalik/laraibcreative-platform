export function getBlogPostById(id) {
    return blogPosts.find(post => post.id === parseInt(id));
}

export function getRelatedPosts(category, currentId) {
    return blogPosts
        .filter(post => post.category === category && post.id !== currentId)
        .slice(0, 3);
}
